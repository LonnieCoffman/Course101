class CircularQueue
  def initialize(size)
    @queue = Array.new(size, nil)
    @position = 0
  end

  def enqueue(value)
    dequeue unless @queue.fetch(-1).nil?
    @queue[@queue.index(nil)] = value
  end

  def dequeue
    removed = @queue.shift
    @queue << nil
    removed
  end
end

